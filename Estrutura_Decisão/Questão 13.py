num_dia = int(input('Informe um numero para saber o dia da semana: '))

if (num_dia == 1):
    print ('Domingo')
elif (num_dia == 2):
    print ('Segunda')
elif (num_dia == 3):
    print ('Terca')
elif (num_dia == 4):
    print ('Quarta')
elif (num_dia == 5):
    print ('Quinta')
elif (num_dia == 6):
    print ('Sexta')
elif (num_dia == 7):
    print ('Sabado')
else:
    print ('Valor invalido')
