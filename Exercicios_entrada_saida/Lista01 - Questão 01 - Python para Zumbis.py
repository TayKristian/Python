"""
Faça um programa que peça dois números inteiros e imprima a soma desses dois números.
Comentário de multiplas linhas
"""
# n1 -> número 1
n1 = int(input("Digite o 1° número: "))
# n2 -> número 2
n2 = int(input("Digite o 2° número: "))
# s -> soma
s = n1 + n2

print('A soma é', s)
